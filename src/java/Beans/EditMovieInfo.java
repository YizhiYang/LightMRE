public class EditMovieInfo {
    
    private String id;
    private String name;
    private String type;
    private int rating;
    private double distrFee;
    private String numberOfCopies;
    private String actorName;
    private String actorAge;
    private String actorGender;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public int getRating() {
        return rating;
    }

    public void setRating(int rating) {
        this.rating = rating;
    }

    public double getDistrFee() {
        return distrFee;
    }

    public void setDistrFee(double distrFee) {
        this.distrFee = distrFee;
    }

    public String getNumberOfCopies() {
        return numberOfCopies;
    }

    public void setNumberOfCopies(String numberOfCopies) {
        this.numberOfCopies = numberOfCopies;
    }

    public String getActorName() {
        return actorName;
    }

    public void setActorName(String actorName) {
        this.actorName = actorName;
    }

    public String getActorAge() {
        return actorAge;
    }

    public void setActorAge(String actorAge) {
        this.actorAge = actorAge;
    }

    public String getActorGender() {
        return actorGender;
    }

    public void setActorGender(String actorGender) {
        this.actorGender = actorGender;
    }
    
   
}
